#SP 6th Old Enough 

age = int(input("How old are you?"))

if age >= 18: 
    print("You are old enough to vote!")
elif age >= 16:
    print("You are old enough to be able to drive!") 
elif age >= 15:
    print("You are old enough to get a learners permit!")
else:
    print("You are old enough to be able to go to school!")